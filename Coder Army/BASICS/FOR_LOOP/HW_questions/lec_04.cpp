/* 1__print  numer 280 to 250 */

#include<iostream>
using namespace std;

int main()
{
    int  n;
    

    for(n=280;n>=270;n=n-1)
    {
        cout<< n << endl;

    }
    return 0;


}  

/* 2__print char A to Z  */

#include<iostream>
using namespace std;

int main()
{
    char n;
    

    for(n='A';n<='Z';n=n+1)
    {
        cout<< n << endl;

    }
    return 0;


}  

/* 3__print char Z to A */

#include<iostream>
using namespace std;

int main()
{
    char n;
    

    for(n='Z';n>='A';n=n-1)
    {
        cout<< n << endl;

    }
    return 0;


}  