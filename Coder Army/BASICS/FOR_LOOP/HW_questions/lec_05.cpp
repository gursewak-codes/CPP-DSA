/* 1__print  numer 220 to 730  leaving 7 number every times */

#include<iostream>
using namespace std;

int main()
{
    int  n;
    

    for(n=220;n<=730;n=n+7)
    {
        cout<< n << endl;

    }
    return 0;


}  


